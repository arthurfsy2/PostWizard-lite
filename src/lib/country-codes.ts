/**
 * 标准化国家名称（保留 Postcrossing 官方格式）
 * @param countryName 原始国家名称
 * @returns Postcrossing 官方名称，如果找不到则返回清理后的原始名称
 */
export function normalizeCountryName(countryName: string): string {
  const lower = countryName.toLowerCase().trim();
  
  // 1. 先检查是否是缩写
  const code = ABBREVIATION_TO_CODE[lower];
  if (code && COUNTRY_NAMES[code]) {
    return COUNTRY_NAMES[code];
  }
  
  // 2. 查找是否匹配官方名称
  for (const [code, name] of Object.entries(COUNTRY_NAMES)) {
    if (name.toLowerCase() === lower) {
      return name;
    }
  }
  
  // 3. 找不到映射，返回清理后的原始名称（大写）
  return countryName.replace(/\s+(has|have|arrived|delivered)$/i, '').trim().toUpperCase();
}
