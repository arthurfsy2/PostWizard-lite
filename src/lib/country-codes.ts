/**
 * 根据国家名称获取 2 字母代码
 * @param countryName 国家名称（支持英文全称、缩写）
 * @returns 2 字母国家代码，如果找不到则返回 undefined
 */
export function getCountryCode(countryName: string): string | undefined {
  const lower = countryName.toLowerCase().trim();
  
  // 1. 先检查是否是缩写
  const code = ABBREVIATION_TO_CODE[lower];
  if (code) {
    return code;
  }
  
  // 2. 反向查找 COUNTRY_NAMES
  for (const [code, name] of Object.entries(COUNTRY_NAMES)) {
    if (name.toLowerCase() === lower) {
      return code;
    }
  }
  
  return undefined;
}

/**
 * 根据国家代码获取官方名称
 * @param code 2 字母国家代码
 * @returns Postcrossing 官方名称，如果找不到则返回 undefined
 */
export function getCountryName(code: string): string | undefined {
  return COUNTRY_NAMES[code.toUpperCase()];
}

